#include <iostream>
using namespace std;

/*
 * Программа для работы с одномерным массивом
 * Находит сумму отрицательных элементов, расположенных между максимальным и минимальным
 */

int main() {
    setlocale(LC_ALL, "Russian");

    int n;
    double a[100]; // Объявление массива

    cout << "Введите количество элементов массива (до 100): ";
    cin >> n;

    // Проверка входных данных
    if (cin.fail() || n <= 0 || n > 100) {
        cout << "Ошибка! Недопустимый размер массива. Программа завершена\n";
        return 0;
    }

    // Заполнение массива данными от пользователя
    for (int i = 0; i < n; i++) {
        cout << "Введите " << i + 1 << " элемент массива: ";
        cin >> a[i];

        // Защита от ввода неверных символов
        if (cin.fail()) {
            cout << "Ошибка! Нужно было ввести число. Программа завершена\n";
            return 0;
        }
    }

    // Поиск индексов минимального и максимального элементов
    int min_idx = 0;
    int max_idx = 0;
    for (int i = 1; i < n; i++) {
        if (a[i] < a[min_idx]) min_idx = i; // Обновляем индекс минимального
        if (a[i] > a[max_idx]) max_idx = i; // Обновляем индекс максимального
    }

    // Вывод промежуточных результатов
    cout << "\n-----------------------------------------\n";
    cout << "Минимальный элемент на позиции: " << min_idx + 1 << " (число " << a[min_idx] << ")\n";
    cout << "Максимальный элемент на позиции: " << max_idx + 1 << " (число " << a[max_idx] << ")\n";

    // Определение границ диапазона (кто из них идет раньше в массиве)
    int start, end;
    if (min_idx < max_idx) {
        start = min_idx;
        end = max_idx;
    }
    else {
        start = max_idx;
        end = min_idx;
    }

    double sum = 0;
    bool found = false; // Флаг наличия отрицательных чисел в диапазоне

    // Алгоритм поиска суммы отрицательных элементов строго между min и max
    for (int i = start + 1; i < end; i++) {
        if (a[i] < 0) {
            sum = sum + a[i];
            found = true;
        }
    }

    // Финальный вывод результата
    if (found) {
        cout << "Сумма отрицательных чисел между ними: " << sum << endl;
    }
    else {
        cout << "Между ними нет отрицательных чисел или элементов вовсе\n";
    }

    return 0;
}
